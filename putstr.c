#include "ft_printf.h"

static void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	return (1);
}
